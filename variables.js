// A variable is a name storage for data.
// We declare a variable with the "LET" key word
// The let key word is for variables that hold values that can be changed.
let message = 'Hello Tendo !';
console.log(message)

let firstName = 'Tendo';
console.log(typeof firstName);

firstName = 100;
console.log(typeof firstName);

firstName = true;
console.log(typeof firstName);

let name = 'Christabel';
const country = 'Uganda';
var age = 20;
console.log(name, country, age);

let university = "UCU";
const yearStarted = 2023;
var isGraduate = true;
console.log(university, yearStarted, isGraduate);

